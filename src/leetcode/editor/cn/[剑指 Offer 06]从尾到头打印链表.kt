/**
 * Example:
 * var li = ListNode(5)
 * var v = li.`val`
 * Definition for singly-linked list.
 * class ListNode(var `val`: Int) {
 *     var next: ListNode? = null
 * }
 */
class Solution {
    fun reversePrint(head: ListNode?): IntArray {
        var stack = Stack<Int>()
        var cur = head
        while (cur != null) {
            stack.push(cur.`val`)
            cur = cur.next
        }
        var array = IntArray(stack.size)
        for (i in 0 until stack.size) {
            array[i] = stack.pop()
        }
        return array
    }
}
