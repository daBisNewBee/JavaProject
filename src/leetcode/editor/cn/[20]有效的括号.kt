    fun isValid(s: String): Boolean {
        if (s.isEmpty() || s.length % 2 == 1) return false
        var array = s.toCharArray()

        var map = mapOf(
                ')' to '(',
                ']' to '[',
                '}' to '{')
        var stack = Stack<Char>()
        for(c in array) {
            if (map.values.contains(c)) {
                stack.push(c)
            } else {
                if (stack.isEmpty() || stack.pop() != map[c]) {
                    return false
                }
            }
        }
        return stack.isEmpty()
    }
}
