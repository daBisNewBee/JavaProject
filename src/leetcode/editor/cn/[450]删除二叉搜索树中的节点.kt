class P_450_DeleteNodeInABst {
    //leetcode submit region begin(Prohibit modification and deletion)
/**
 * Example:
 * var ti = TreeNode(5)
 * var v = ti.`val`
 * Definition for a binary tree node.
 * class TreeNode(var `val`: Int) {
 *     var left: TreeNode? = null
 *     var right: TreeNode? = null
 * }
 */
class Solution {
    fun deleteNode(root: TreeNode?, key: Int): TreeNode? {
        root?:return null

        if (key < root.`val`) {
            root.left = deleteNode(root.left, key)
        } else if (key > root.`val`) {
            root.right = deleteNode(root.right, key)
        } else {
            // å½“å‰èŠ‚ç‚¹å°±æ˜¯è¦åˆ é™¤çš„èŠ‚ç‚¹
            root.left ?: return root.right // æ— å·¦å­æ ‘
            root.right ?: return root.left // æ— å³å­æ ‘
            // æœ‰å³å­æ ‘ã€ä¹Ÿæœ‰å·¦å­æ ‘
            var tmp = root.right
            while (tmp?.left != null) {
                tmp = tmp.left
            }
            // tmp æ˜¯å³å­æ ‘çš„æœ€å·¦èŠ‚ç‚¹
            tmp?.left = root.left // å·¦å­æ ‘æ”¾åˆ°å³å­æ ‘çš„æœ€å·¦å­èŠ‚ç‚¹
            return root.right
