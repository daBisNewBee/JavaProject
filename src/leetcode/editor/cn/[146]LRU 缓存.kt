class LRUCache(capacity: Int) {

    private var cache = mutableMapOf<Int, MyNode>()
    private var head = MyNode()
    private var tail = MyNode()
    private var capacity = capacity

    init {
        head.next = tail
        tail.pre = head
    }

    fun get(key: Int): Int {
        if (!cache.containsKey(key)) {
            return -1
        }
        var ans = cache[key]!!
        removeNode(ans)
        add2First(ans)
        return ans.value
    }

    private fun add2First(node: MyNode) {
        cache[node.key] = node
        node.next = head.next
        head.next?.pre = node
        head.next = node
        node.pre = head
    }

    private fun removeNode(node: MyNode) {
        cache.remove(node.key)
        node.pre?.next = node.next
        node.next?.pre = node.pre
    }

    private fun removeLast() {
        removeNode(tail.pre!!)
    }


    fun put(key: Int, value: Int) {
        var node:MyNode
        if (cache.containsKey(key)) {
            node = cache[key]!!
            // ä¸è¦å¿˜äº†æ›´æ–°value
            node.value = value
            removeNode(node)
        } else {
            node = MyNode(key = key, value = value)
        }
        add2First(node)
        if (cache.size > capacity) {
            removeLast()
        }
    }

        private class MyNode (var next:MyNode? = null, var pre:MyNode? = null, var key:Int = 0, var value: Int = 0)
}

