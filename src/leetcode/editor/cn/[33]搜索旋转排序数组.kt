             */
            // 1. å…ˆç¡®å®šæœ‰åºæ•°ç»„ï¼šè¦ä¹ˆå·¦è¾¹ï¼Œè¦ä¹ˆå³è¾¹ï¼Œå¿…ç„¶æœ‰ä¸€ä¸ªåŒºåŸŸæœ‰åºï¼
            // 2. å¹¶æ ¹æ®æœ‰åºçš„é‚£ä¸ªéƒ¨åˆ†ç¡®å®šæˆ‘ä»¬è¯¥å¦‚ä½•æ”¹å˜äºŒåˆ†æŸ¥æ‰¾çš„ä¸Šä¸‹ç•Œ
            if (nums[p1] <= nums[mid]) {
                // æ³¨æ„å¼€é—­åŒºé—´çš„é€‰æ‹©ï¼å¯ä»¥è”ç³»ä¸‹é¢åŒºé—´çš„æ›´æ–°ã€‚
                // æ¯”å¦‚"p2 = mid-1"ï¼Œå·²ç»æŠŠä½äºmidçš„æ•°å­—æ’é™¤åœ¨å¤–äº†ã€‚
                // é‚£åœ¨åˆ¤æ–­æ—¶ï¼Œ"target < nums[mid]"å¿…ç„¶å°±ä¸èƒ½åŒ…æ‹¬è¾¹ç•Œ
                if (target >= nums[p1] && target < nums[mid]) {
                    p2 = mid-1
                } else {
                    p1 = mid + 1
                }
            } else {
                if (target > nums[mid] && target <= nums[p2]) {
                    p1 = mid+1
                } else {
                    p2 = mid-1
                }
            }
        }
        return -1
    }
}
