class Solution {
    fun search(nums: IntArray, target: Int): Int {
        if (nums.size == 1) return if (nums[0] == target) 0 else -1
        var p1 = 0

        var p2 = nums.size-1


        while (p1 <= p2) {
            var mid = p1 + (p2-p1) / 2

            if (target == nums[mid]) {
                return mid
            }

            /**
