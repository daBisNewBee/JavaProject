        fun dfs_Stack(root: Node?):Node? {
            if (root == null) return null

            var stack = Stack<Node>()
            var cur :Node ?= root
            while (stack.isNotEmpty() || cur != null) {
                while (cur != null) {
                    stack.push(cur)
                    cur = cur.left
                }
                cur = stack.pop()

                // visit å½“å‰èŠ‚ç‚¹
